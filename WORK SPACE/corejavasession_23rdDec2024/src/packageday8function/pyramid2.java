package packageday8function;

public class pyramid2 {

	public static void main(String[] args) {
	        System.out.println("Program Starts");
	        for(int i=6;i>=1;i--) {
	        	for(int j=1;j<i;j++) {
	        		System.out.print(i+"");
	        	}
	        	System.out.println();
	        }
	        System.out.println("*************************");
	        for(int i=6;i>=1;i--) {
	        	for(int j=1;j<=i;j++) {
	        		System.out.println("*******************************");
	        	}
	        	System.out.println();
	        	
	        }
	        System.out.println("Program Ends");
	}

}

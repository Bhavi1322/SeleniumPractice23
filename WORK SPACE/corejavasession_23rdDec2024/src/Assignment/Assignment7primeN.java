package Assignment;

public class Assignment7primeN {

	public static void main(String[] args) {
		    int num=100,ct=0;
		    for(int i=1;i<=num;i++) {
		    	    if(num%i==0) {
		    	            ct++;	
		    	    }
		    }   	
		    if(ct==2) {
		    	  System.out.println("Given number is prime:"+num);
		    }else {
		    	  System.out.println("Given number is not prime:"+num);
		    	
		    	
		    	
		    	
		    	    	
	
		    }                

	}

}
